	<h2>Membership Benefits</h2>

	<h3>E-mail Forum</h3>
	<p>The AMIT e-mail forum is a great way to be in contact with other Maine interpreters and translators. One message goes to every member. You will be in contact with interpreters and translators all over Maine. You can ask or answer questions on terminology and share important information on programs, employment and other topics. The moderators will post job opportunities from other lists as well as upcoming information on seminars, conferences, courses and training programs being offered.</p>

	<h3>Interpreting and Translating Trainings and Workshops</h3>

	<h3>Member Discounts on Programs Offered By or Sponsored by AMIT</h3>

	<h3>AMIT Newsletter</h3>
	<p>A publication put together on a voluntary basis by AMIT members for AMIT and the broader community. Contributions welcome!</p>

	<h3>Monthly Meetings</h3>
	<p>Come see us and enjoy the company and collegial support of other translators and interpreters. AMIT holds monthly meetings at Eastern Maine Medical Center in Bangor. Group discussions include topics pertinent to translating and interpreting, planning events, how to increase exposure and market yourself, and details on upcoming workshops and trainings. If you can't make it, we will keep all members informed via e-mail and the web site.</p>

	<h3>Upcoming Event Reminders</h3>
	<p>Regular notification and information on important educational programs, conferences, seminars of other associations, and carpooling to those events.<p>

	<h3>Professional Support</h3>
	<p>Collegial support and networking among other interpreters and translators.</p>

	<h3>AMIT Website</h3>
	<p>Your own listing on our website with contact information, language, specialties, and a brief biography. Additionally, access to resources, links, and other public information through the website.</p>

